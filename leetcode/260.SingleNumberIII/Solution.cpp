class Solution {
public:
vector<int> singleNumber(vector<int>& nums) {
    vector<int> result({0,0});
    int xor_tmp = 0;
    for(int i = 0; i < nums.size(); i++){
        xor_tmp = xor_tmp ^ nums[i];
    }
    int flag = xor_tmp & (- xor_tmp);//这一句:求出不同的一位,负数为补码,按位取反再加1
    for(int i = 0; i < nums.size(); i++){
        if(nums[i] & flag)
            result[0] = result[0] ^ nums[i];
        else 
            result[1] = result[1] ^ nums[i];
    }
    return result;
}
};

